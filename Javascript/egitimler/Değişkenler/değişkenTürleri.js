//Değişkenler, İçerisinde her türlü bilgi veya veriyi depolamak için kullanılan alanlara verilen isimlerdir.

//var = Standart Değişken tanımlamakta kullanılır.
var a = "Standart Değişken";
document.writeln(a);

//let = Blok kapsamlı yerel değişken tanımlamakta kullanılır.
let b = "Blok Değişken";
document.writeln(b);

//const = Sabit değişken tanımlamakta kullanılır.
const c = "Sabit Değişken";
document.writeln(c);
document.writeln("<br><br>");
document.writeln(a + b + c);

// charAt() Metodu
// Kendisine parametre olarak verilen değer doğrultusunda değişken içeriğindeki karşılığı olan karakteri bularak geriye döndürür.

//İçerikteki her karakterin bir sıra numarası vardır ve 0'dan saymaya başlar.
var deger1 = "Javascript Eğitim Seti";
document.write(deger1);
document.write("<br><br>");

var sonuc1 = deger1.charAt(0);
var sonuc2 = deger1.charAt(11);
document.write(sonuc1 + "<br>");
document.write(sonuc2);
document.write("<br><br>");

//JS'de herhangi bir değişken içerisindeki alanlara ulaşmak istiyorsak bu metodu kullanıyoruz.
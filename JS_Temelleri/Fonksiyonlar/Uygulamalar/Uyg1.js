function topla(a,b) {
    return a + b;
}
function cikar(a,b) {
    return a - b;
}
function carp(a,b) {
    return a * b;
}
function bol(a,b) {
    return a / b;
}
function kare(x) {
    return x*x;
}
function kup(y) {
    return y*y*y;
}


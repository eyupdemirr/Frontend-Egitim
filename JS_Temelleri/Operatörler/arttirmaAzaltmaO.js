//Arttırma ve Azaltma Operatörleri
//(++x, x++, --x, x--)

x1 = Number(prompt("Lütfen Bir Sayi Giriniz= "));
x2 = Number(prompt("Lütfen Bir Sayi Giriniz= "));
x3 = Number(prompt("Lütfen Bir Sayi Giriniz= "));
x4 = Number(prompt("Lütfen Bir Sayi Giriniz= "));

document.write(x1++ + "<br>");
document.write(x1 + "<br>");

document.write("<br>");

document.write(++x2 + "<br>");
document.write(x2 + "<br>");

document.write("<br>");

document.write(x3-- + "<br>");
document.write(x3 + "<br>");

document.write("<br>");

document.write(--x4 + "<br>");
document.write(x4 + "<br>");

